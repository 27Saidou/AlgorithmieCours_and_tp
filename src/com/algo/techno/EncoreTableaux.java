package com.algo.techno;



public class EncoreTableaux {

	public static void main(String[] args) {
		/**
		 * declaration de tableaux
		 */
		int[][]C= {{1,2,3},{4,5,6}};
		
		for (int i = 0; i <C.length; i++) {
			for (int j = 0; j < C[i].length; j++) {
				System.out.println(C[i][j]+" ");
			}
			
		}
	}

}
