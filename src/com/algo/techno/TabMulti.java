package com.algo.techno;

public class TabMulti {

	public static void main(String[] args) {
	int [][]notes= {{5,3,6,15,3},{3,8,4},{18,14,2,9}};
	for (int i = 0; i < notes.length; i++) {
		for (int j = 0; j < notes.length; j++) {
			System.out.println(notes[i][j]+"");
		}
		System.out.println();
	}
	}

}
