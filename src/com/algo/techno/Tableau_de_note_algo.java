package com.algo.techno;

import java.util.LinkedList;


public class Tableau_de_note_algo {

	public static void main(String[] args) {
		
		LinkedList<String>cl=new LinkedList<>();
		cl.add("java");
		cl.add("python");
		cl.add("c++");
		cl.add("php");
		cl.add("js");
		for (String s : cl) {
			System.out.println(s);
		}
		
		
	}

}
